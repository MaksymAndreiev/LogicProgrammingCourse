% Правило для призначення стипендії
% Визначає, чи має студент право на підвищену чи звичайну стипендію,
% або чи він відраховується з університету.
scholarship(student(Name, AverageGrade), Scholarship) :-
    (AverageGrade >= 4.5 ->
        Scholarship = "підвищену стипендію"
    ; AverageGrade >= 3 ->
        Scholarship = "звичайну стипендію"
    ; Scholarship = "відрахування з університету"
    ).
